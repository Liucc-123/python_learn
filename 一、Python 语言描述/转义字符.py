# \t制表符
print("jack\t20")

# \n换行
print("Hello,jack\nhello,tom")

# \\输出\
print("D:\\Pycharm\\chapter02")

# \"输出"     \'输出'
print("郭靖说：\"hello\"")
print("郭靖说：\'hello\'")  # 单引号也可以不使用转义 \

# \r回车
print("不是一个人的王者, \r而是团队的荣耀")

